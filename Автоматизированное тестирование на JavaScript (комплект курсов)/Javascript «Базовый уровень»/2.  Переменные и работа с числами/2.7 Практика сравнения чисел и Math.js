/*
Задание 2: сравниваем два дробных числа с указанной точностью.

Даны 2 числа и кол-во знаков после запятой, которое необходимо учитывать.

нужно вывести информацию: равны ли эти два числа? Если нет, то больше первое или второе? 
*/

let first = 0.1 + 0.2 + 0.033; //0.333
let second = 0.3334;
let precision = 3; //тут указали точность

let firstNormalized = Math.round(
    first * Math.pow(10, precision) //умножаем наше число на 10 в степени 3 (т.к. мы указали точность вычисления 3 знака после запятой). А затем откидываем все что после запятой  
    );
let secondNormalized = Math.round(
    second * Math.pow(10, precision)
    );

console.log('Исходные числа равны', first === second);
console.log('Числа равны с заданной точностью', firstNormalized === secondNormalized);
console.log('Первое число больше', firstNormalized > secondNormalized);
console.log('Второе число больше', firstNormalized < secondNormalized);

