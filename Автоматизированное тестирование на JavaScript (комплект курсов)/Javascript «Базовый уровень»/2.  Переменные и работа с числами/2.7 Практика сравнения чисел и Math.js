/*
Задание 1: вычислим растояние между двумяточками.

Даны координаты x, y 2-х точек. Нужно вывести растояние между ними.
Вычисляем по теореме Пифагора.

курс геометрии в помощь: для того, чтобы найти растояние между точками - нужно построить прямоугольный треуголник, 
где гипотенуза этого треугольникаи будет являться растоянием между этими двумя точками.

Дальше гипотенуза ищется по теореме Пифагора
*/

//координаты первой точки
let x1 = 10;
let y1 = 2;

//координаты второй точки
let x2 = -3;
let y2 = 3;

//т.к. заранее мы не знаем какие значения в X и Y у нас больше друг друга, мы используем абсолютную величину 
let cathetus1 = Math.abs(x1 - x2); //значение первого катета
let cathetus2 = Math.abs(y1 - y2); //значение второго катета

//нам нужна гипотенуза, которая по теореме Пифагора равна квадратному корню от суммы двух квадратов катетов 
console.log(Math.sqrt(
    Math.pov(cathetus1, 2) + Math.pow(cathetus2, 2)
    )
);

